<div id="vsb_content"><p class="vsbcontent_start">6月13日（周三）下午，巴黎高科集团（ParisTech）驻华主任Sylvain FERRARI先生将访问我校，向我校学生全面介绍巴黎高科的留学机会。 </p>
<p>有意赴该校攻读硕博项目的同学，欢迎踊跃参加！ </p>
<p>【时间】2018年6月13日（星期三）15 :00-16 :00 </p>
<p>【地点】学院路校区新主楼会议中心第二会议室 </p>
<p>【主讲人】巴黎高科集团（ParisTech）驻华主任Sylvain FERRARI先生 </p>
<p>【对象】理工类、经济类本科及硕士生 </p>
<p><strong><span style="font-size: 14px">学校简介</span></strong></p>
<p>巴黎高科（ParisTech）汇集了全法最具声望的10所工程师学校，每所学校都在不同的专业领域享誉盛名，其教学、科研范围覆盖了工程学、理学和经济学的所有学科，旨在培养杰出的研究型及管理型人才。</p>
<p><strong><span style="font-size: 14px">巴黎高科成员学校</span></strong></p>
<p>巴黎高科环境与生命科学工程学院(Agro ParisTech) </p>
<p>巴黎高科国立高等工程技术学校(Arts et M&eacute;tiers ParisTech)</p>
<p>巴黎高科国立化学学校(Chimie ParisTech)</p>
<p>巴黎高科国立桥路学校(Ecole des Ponts ParisTech)</p>
<p>巴黎高科国立统计与经济管理学校( ENSAE ParisTech)</p>
<p>巴黎高科高等先进技术学校(ENSTA ParisTech)</p>
<p>巴黎高科高等理工化工学校(ESPCI Paris)</p>
<p><a href="http://baike.baidu.com/view/3978584.htm">高等光学学院</a>（Institut d'Optique Graduates School）</p>
<p>巴黎高科国立巴黎高等矿业学校(MINES ParisTech)</p>
<p>巴黎高科<a href="http://baike.baidu.com/view/4247753.htm">国立高等电信学校</a>(T&eacute;l&eacute;com ParisTech)</p>
<p class="vsbcontent_img">
 <img border="0" src="../../_mediafile/xwwx/2018/06/11/2e1w2iarr8.jpg" orisrc="/_mediafile/xwwx/2018/06/11/source/2e1w2iarr8.jpg" class="img_vsb_content"></p>
<p><strong><span style="font-size: 14px">巴黎高科各校教学领域</span></strong></p>
<p class="vsbcontent_end">应用数学–计算机科学–统计学–金融–物理学–光学科学–经济与社会科学–工程学-工业与生产工程–管理–材料科学与工程–机械工程–海洋工程–船建工程–电气、电子与光子工程–信息技术与电信–机器人–自动化–能源与核工程–化学–化工–地球科学–环境工程–生命科学–食品工程–森林工程–农业工程–计算生物学–生物工程和生物物理 </p>
