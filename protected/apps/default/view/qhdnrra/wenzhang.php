	function ScrollToControl(id){
		var scrollPos = document.getElementById("content").contentWindow.document.getElementById(id).offsetTop + 400;
		//获取当前滚动条位置
		var currentPos;
		
		if(window.pageYOffset) 
	    {    // all except IE    
			currentPos = window.pageYOffset;    
	    } else if(document.documentElement && document.documentElement.scrollTop) 
	    {    // IE 6 Strict    
	    	currentPos = document.documentElement.scrollTop;    
	    } else if(document.body) {    // all other IE    
	    	currentPos = document.body.scrollTop;    
	    } 
		
		if(scrollPos > currentPos){
			window.scrollBy(0,scrollPos - currentPos);
		}else{
			window.scrollBy(0,-(currentPos - scrollPos));
		}
	}
	
	//自适应iframe高度
	function IFrameReSize(){
   		var a=document.getElementById("content");
   		a.height = a.contentDocument.body.scrollHeight; 
	}
	
